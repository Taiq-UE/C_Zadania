#include <stdio.h>
#include <string.h>

int main() {
    int cont = 1;
    int p1Wins = 0;
    int p2Wins = 0;
    int counter = 0;

    char strings[100][2][10];

    while (cont == 1) {

        printf("Player 1 move: \n1. Rock\n2. Paper\n3. Scissors\n4. Scores\nPlayer 1: ");
        char choice1 [10];
        scanf("%s", choice1);
        char choice2 [10];
        printf("Player 2: ");
        scanf("%s", choice2);

        strcpy(strings[counter][0], choice1);
        strcpy(strings[counter][1], choice2);
        counter++;

        if (strcmp(choice1, "Rock") == 0 && strcmp(choice2, "Paper") == 0) {
            printf("Player 2 wins\n");
            p2Wins++;
        }
        else if (strcmp(choice1, "Paper") == 0 && strcmp(choice2, "Scissors") == 0) {
            printf("Player 2 wins\n");
            p2Wins++;
        }
        else if (strcmp(choice1, "Scissors") == 0 && strcmp(choice2, "Rock") == 0) {
            printf("Player 2 wins\n");
            p2Wins++;
        }
        else if (strcmp(choice1, "Rock") == 0 && strcmp(choice2, "Scissors") == 0) {
            printf("Player 1 wins\n");
            p1Wins++;
        }
        else if (strcmp(choice1, "Paper") == 0 && strcmp(choice2, "Rock") == 0) {
            printf("Player 1 wins\n");
            p1Wins++;
        }
        else if (strcmp(choice1, "Scissors") == 0 && strcmp(choice2, "Paper") == 0) {
            printf("Player 1 wins\n");
            p1Wins++;
        }
        else if (strcmp(choice1, "Scores") == 0 || strcmp(choice2, "Scores") == 0) {
            printf("Player 1 score: %d\nPlayer 2 score: %d\n", p1Wins, p2Wins);

            for (int i = 0; i < counter-1; i++) {
                printf("Player 1: %s\nPlayer 2: %s\n\n", strings[i][0], strings[i][1]);
            }

            cont = 0;
        }
        else {
            printf("Tie\n");
        }
    }
    return 0;
}